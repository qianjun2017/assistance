package com.cc.map.http.response.model;

public class IpLocationContentPoint {

	/**
	 * 当前城市中心点经度
	 */
	private String x;
	
	/**
	 * 当前城市中心点纬度
	 */
	private String y;

	/**
	 * @return the x
	 */
	public String getX() {
		return x;
	}

	/**
	 * @param x the x to set
	 */
	public void setX(String x) {
		this.x = x;
	}

	/**
	 * @return the y
	 */
	public String getY() {
		return y;
	}

	/**
	 * @param y the y to set
	 */
	public void setY(String y) {
		this.y = y;
	}
}
